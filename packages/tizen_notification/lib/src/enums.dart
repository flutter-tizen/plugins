/// The type of sound.
enum SoundType {
  /// No sound.
  none,

  /// Default sound.
  builtIn,

  /// User sound data.
  userData,
}

/// The type of image.
enum ImageType {
  /// No image.
  none,

  /// Icon.
  icon,

  /// Indicator icon.
  iconForIndicator,

  /// Lock screen icon.
  iconForLock,
}

/// The type of vibration.
enum VibrationType {
  /// No vibration.
  none,

  /// Default vibrate pattern.
  builtIn,

  /// User vibration data.
  userData,
}
