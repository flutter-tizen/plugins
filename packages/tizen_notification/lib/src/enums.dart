/// The type of sound.
enum SoundType {
  /// No sound.
  none,

  /// Default sound.
  builtIn,

  /// User sound data.
  userData,
}

/// The type of vibration.
enum VibrationType {
  /// No vibration.
  none,

  /// Default vibrate pattern.
  builtIn,

  /// User vibration data.
  userData,
}
